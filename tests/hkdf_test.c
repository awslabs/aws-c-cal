/* Test vectors from rfc5869 converted to use SHA512. */

static int s_hkdf_derive_test_case_1(struct aws_allocator *allocator, void *ctx) {
    (void)ctx;

#if defined(AWS_OS_LINUX)
#    if defined(OPENSSL_IS_OPENSSL)
#        if OPENSSL_VERSION_NUMBER < 0x10100000
    return AWS_OP_SKIP;
#        endif
#    endif
#endif

    aws_cal_library_init(allocator);

    uint8_t ikm[] = {0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
                     0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b};
    struct aws_byte_cursor ikm_cur = aws_byte_cursor_from_array(ikm, sizeof(ikm));

    uint8_t salt[] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c};
    struct aws_byte_cursor salt_cur = aws_byte_cursor_from_array(salt, sizeof(salt));

    uint8_t info[] = {0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7, 0xf8, 0xf9};
    struct aws_byte_cursor info_cur = aws_byte_cursor_from_array(info, sizeof(info));

    struct aws_byte_buf ret;
    aws_byte_buf_init(&ret, allocator, 64);

    ASSERT_SUCCESS(aws_hkdf_derive(allocator, HKDF_HMAC_SHA512, ikm_cur, salt_cur, info_cur, &ret, 42));

    uint8_t expected[] = {0x83, 0x23, 0x90, 0x08, 0x6c, 0xda, 0x71, 0xfb, 0x47, 0x62, 0x5b, 0xb5, 0xce, 0xb1,
                          0x68, 0xe4, 0xc8, 0xe2, 0x6a, 0x1a, 0x16, 0xed, 0x34, 0xd9, 0xfc, 0x7f, 0xe9, 0x2c,
                          0x14, 0x81, 0x57, 0x93, 0x38, 0xda, 0x36, 0x2c, 0xb8, 0xd9, 0xf9, 0x25, 0xd7, 0xcb};

    ASSERT_BIN_ARRAYS_EQUALS(ret.buffer, ret.len, expected, sizeof(expected));

    aws_byte_buf_clean_up(&ret);

    aws_cal_library_clean_up();

    return AWS_OP_SUCCESS;
}
AWS_TEST_CASE(hkdf_derive_test_case_1, s_hkdf_derive_test_case_1);

